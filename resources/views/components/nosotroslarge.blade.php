        <x-titulo-principal>PLAN DE ESTUDIOS</x-titulo-principal>
        <p class="lead mb-5 text-lg p-6" style="text-align:justify;">
            Habilitaciones profesionales para el “Técnico Químico” - Resolución Nº 4149/SSGECP/2012<br>
            Del análisis de las actividades profesionales que se desprenden del Perfil Profesional, se establecen como habilitaciones para el Técnico Químico:<br>
            <b>A.</b>   Supervisar y realizar ensayos en el ámbito industrial.<br>
            <b>B.</b>   Investigar, programar, dirigir, realizar y/o asesorar respecto de la fabricación y utilización de productos químicos-industriales o procesos industriales en su faz específicamente química.<br><br>
            
            Con lo cual el Técnico Químico está capacitado para:<br>
            <b>1.</b>   Inventariar elementos, drogas y reactivos del laboratorio.<br>
            <b>2.</b>   Efectuar mantenimiento y limpieza en equipos e instrumentos.<br>
            <b>3.</b>   Realizar análisis químicos, físicos, fisicoquímicos y microbiológicos.<br>
            <b>4.</b>   Llevar a cabo e interpretar análisis instrumental.<br>
            <b>5.</b>   Operar y administrar laboratorios y plantas.<br>
            <b>6.</b>   Interpretar y aplicar técnicas de muestreo, tanto de toma como de preparación de la muestra.<br>
            <b>7.</b>   Efectuar el seguimiento y control de ensayos realizados en planta y de equipos específicos.<br>
            </p>
